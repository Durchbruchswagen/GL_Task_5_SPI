/* USER CODE BEGIN 0 */
uint32_t Map_Tilt_To_PWM(float angle) {
    if (angle < DEADZONE) return 0;
    if (angle >= MAX_TILT) return (uint32_t)PWM_MAX;

    return (uint32_t)(((angle - DEADZONE) / (MAX_TILT - DEADZONE)) * PWM_MAX);
}

void Accel_WriteRegister(uint8_t regAddr, uint8_t value) {
    uint8_t controlByte = regAddr & WRITE;

    HAL_GPIO_WritePin(CS_I2C_SPI_GPIO_Port, CS_I2C_SPI_Pin, GPIO_PIN_RESET);
    HAL_SPI_Transmit(&hspi1, &controlByte, 1, 10);
    HAL_SPI_Transmit(&hspi1, &value, 1, 10);
    HAL_GPIO_WritePin(CS_I2C_SPI_GPIO_Port, CS_I2C_SPI_Pin, GPIO_PIN_SET);
}

void Accel_ReadXYZ(int16_t* pData) {
    uint8_t rawBuffer[6];
    uint8_t controlByte = LIS3DSH_OUT_X_L | READ;

    HAL_GPIO_WritePin(CS_I2C_SPI_GPIO_Port, CS_I2C_SPI_Pin, GPIO_PIN_RESET);

    HAL_SPI_Transmit(&hspi1, &controlByte, 1, 10);
    HAL_SPI_Receive(&hspi1, rawBuffer, 6, 10);

    HAL_GPIO_WritePin(CS_I2C_SPI_GPIO_Port, CS_I2C_SPI_Pin, GPIO_PIN_SET);

    pData[XAXIS] = (int16_t)((rawBuffer[1] << 8) | rawBuffer[0]);
    pData[YAXIS] = (int16_t)((rawBuffer[3] << 8) | rawBuffer[2]);
    pData[ZAXIS] = (int16_t)((rawBuffer[5] << 8) | rawBuffer[4]);
}
